    <td align="left" bgcolor="#FFFFFF"><strong>1、养猪品种没选好</strong><br />

      品种是提高养猪经济效益的首要条件，品种的好坏直接决定了猪的生产性能、饲料消耗量、饲养周期和料肉比等。众多试验表明，饲养优良的杂种猪，可使母猪每窝断乳仔猪增加1～2头，增重提高10%～30%，饲料利用率提高10%～15%以上。</td>

  </tr>

  <tr>

    <td align="left" bgcolor="#FFFFFF"><strong>2、饲料搭配不合理</strong><br />

      饲料是养殖生猪的基础，是养猪成败的关键因素。通常饲料费用占养猪成本的70%～80%，所以怎样合理地选择，利用，开发饲料，提高饲料报酬率，降低耗料率，对提高养猪经济效益起到决定性的作用。<br />

    </td>

  </tr>

  <tr>

    <td align="left" bgcolor="#FFFFFF"><strong>3、猪病防治不到位</strong><br />

      养猪业者总是把注意力盯在猪价上，认为猪价是猪场能否盈利的决定因素，其实不对。如果具体到一个存栏500头母猪、月均出栏800头商品猪的猪场，冬季的一个流行性腹泻，造成的直接损失就是40万元；而猪肉价格如果每千克降1元月均损失8万元，仅相当于流行性腹泻造成损失的五分之一。<br />

    </td>

  </tr>

  <tr>

    <td align="left" bgcolor="#FFFFFF"><strong>4、猪场管理不科学</strong><br />

      饲养管理好的猪场，可以节约饲料，避免饲料浪费，使投入的饲料大限度转化为猪的增重；饲养管理好的猪场，猪病发生的少，既可以节省疫苗购置费用，又可以节约预防用药费用和治疗用药费用等。></td>

